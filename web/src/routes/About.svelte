<div class="card">
  <h2>About Low Frequency Trader</h2>
  <p style="margin-bottom: 1rem;">
    LFT is a real-time portfolio dashboard for algorithmic trading, built with modern web technologies and deployed on Cloudflare's edge network.
  </p>

  <h3 style="margin-top: 1.5rem; margin-bottom: 0.5rem; color: #8b949e;">Architecture</h3>
  <ul style="color: #8b949e; margin-left: 1.5rem; margin-bottom: 1rem;">
    <li>Frontend: Svelte 5 with Chart.js for portfolio visualisation</li>
    <li>Backend: Cloudflare Workers for serverless API endpoints</li>
    <li>Hosting: Cloudflare Pages with automatic deployments</li>
    <li>C++ Trading Engine: Fasthosts VPS for live trading execution</li>
    <li>Data: Alpaca paper trading API integration</li>
  </ul>

  <h3 style="margin-top: 1.5rem; margin-bottom: 0.5rem; color: #8b949e;">Features</h3>
  <ul style="color: #8b949e; margin-left: 1.5rem; margin-bottom: 1rem;">
    <li>Real-time account summary with equity, cash, and buying power</li>
    <li>7-day portfolio history chart with hourly granularity</li>
    <li>Live positions table with unrealised P&L and percentage gains</li>
    <li>Market status indicator (OPEN/CLOSED) with Eastern Time</li>
    <li>Auto-refresh every 60 seconds</li>
  </ul>

  <h3 style="margin-top: 1.5rem; margin-bottom: 0.5rem; color: #8b949e;">Tech Stack</h3>
  <ul style="color: #8b949e; margin-left: 1.5rem; margin-bottom: 1rem;">
    <li><strong>C++26</strong> - Trading logic with constexpr guarantees</li>
    <li><strong>Go</strong> - Data fetching and API integration</li>
    <li><strong>JavaScript</strong> - Cloudflare Workers serverless functions</li>
    <li><strong>Svelte 5</strong> - Reactive frontend framework</li>
    <li><strong>Chart.js</strong> - Portfolio visualisation</li>
    <li><strong>Cloudflare Pages</strong> - Edge-first hosting</li>
    <li><strong>GitHub Pages</strong> - Analysis results publishing</li>
    <li><strong>GitHub Actions</strong> - CI/CD pipeline</li>
  </ul>

